    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String sql="insert into anggota value(?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat=conn.prepareStatement(sql);
            stat.setString(1, tnik.getText());
            stat.setString(2, tnama.getText());
            stat.setString(3, ttempat.getText());
            stat.setString(4, tlahir.getText());
            
            String jkel="";
            if(rblakilaki.isSelected()) jkel="laki-laki";
            else jkel="perempuan";
            stat.setString(5, jkel);
            
            stat.setString(6, talamat.getText());
            stat.setString(7, ttelepon.getText());
            stat.setString(8, ttabungan.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data BERHASIL disimpan");
            kosong();
            tnik.requestFocus();
            datatable();
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data GAGAL disimpan");
        }
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        try{
            String sql="UPDATE anggota set nama=?, tempat=?, tanggal_lahir=?, jenis_kelamin=?, alamat=?, no_tlp=?, tabungan=? where nik=?";
            PreparedStatement stat= conn.prepareStatement(sql);
            
            
            stat.setString(1, tnama.getText());
            stat.setString(2, ttempat.getText());
            stat.setString(3, tlahir.getText());
            
            String jkel="";
            if(rblakilaki.isSelected()) jkel="laki-laki";
            else jkel="Perempuan";
            stat.setString(4, jkel);
            
            stat.setString(5, talamat.getText());
            stat.setString(6, ttelepon.getText());
            stat.setString(7, ttabungan.getText());
            stat.setString(8, tnik.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            tnik.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah"+e);
        }
    }//GEN-LAST:event_bubahActionPerformed

