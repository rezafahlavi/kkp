     protected void datatable(){
         Object[] Baris ={"nik","nama","tempat","tanggal_lahir","jenis_kelamin","alamat","no_tlp","tabungan"};
         tableModel = new DefaultTableModel (null, Baris);
         tblanggota.setModel(tableModel);
         String sql = "select * from anggota";
         try{
            java.sql.Statement stat=conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
             String a = hasil.getString("nik");
             String b = hasil.getString("nama");
             String c = hasil.getString("tempat");
             String d = hasil.getString("tanggal_lahir");
             String e = hasil.getString("jenis_kelamin");
             String f = hasil.getString("alamat");
             String g = hasil.getString("no_tlp");
             String h = hasil.getString("tabungan");
             
             String[] data={a,b,c,d,e,f,g,h};
             tableModel.addRow(data);
            }   
         }catch(Exception e){
         
         }
   
     }
    

