    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        try{
            String sql="UPDATE angsuran set nik=?, nama=?, jumlah_pinjaman=?, angsuran_ke=?, cicilan=?, bayar=?, kembali=? where tanggal=?";
            PreparedStatement stat= conn.prepareStatement(sql);
            
            
            stat.setString(1, tnik.getText());
            stat.setString(2, tnama.getText());
            stat.setString(3, tjumlahpinjaman.getText());
            stat.setString(4, tangsuranke.getText());
            stat.setString(5, tcicilan.getText());
            stat.setString(6, tbayar.getText());
            stat.setString(7, tkembali.getText());
            stat.setString(8, ttanggal.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            ttanggal.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah"+e);
        }
    
    }//GEN-LAST:event_bubahActionPerformed

