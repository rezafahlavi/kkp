     protected void datatable(){
         Object[] Baris ={"tanggal","nik","nama","jumlah_pinjaman","angsuran_ke","cicilan","bayar","kembali"};
         tableModel = new DefaultTableModel (null, Baris);
         tblangsuran.setModel(tableModel);
         String sql = "select * from angsuran";
         try{
            java.sql.Statement stat=conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
             String a = hasil.getString("tanggal");
             String b = hasil.getString("nik");
             String c = hasil.getString("nama");
             String d = hasil.getString("jumlah_pinjaman");
             String e = hasil.getString("angsuran_ke");
             String f = hasil.getString("cicilan");
             String g = hasil.getString("bayar");
             String h = hasil.getString("kembali");
             
             String[] data={a,b,c,d,e,f,g,h};
             tableModel.addRow(data);
            }   
         }catch(Exception e){
         
         }
   
     }

