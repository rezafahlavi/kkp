     protected void datatable(){
         Object[] Baris ={"kode_tabungan","nik","nama","saldo_sekarang","tanggal","tabungan_masuk","tabungan_keluar","saldo"};
         tableModel = new DefaultTableModel (null, Baris);
         tblinputtabungan.setModel(tableModel);
         String sql = "select * from simpanan";
         try{
            java.sql.Statement stat=conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
             String a = hasil.getString("kode_tabungan");
             String b = hasil.getString("nik");
             String c = hasil.getString("nama");
             String d = hasil.getString("saldo_sekarang");
             String e = hasil.getString("tanggal");
             String f = hasil.getString("tabungan_masuk");
             String g = hasil.getString("tabungan_keluar");
             String h = hasil.getString("saldo");
             
             String[] data={a,b,c,d,e,f,g,h};
             tableModel.addRow(data);
            }   
         }catch(Exception e){
         
         }
   
     }
    

