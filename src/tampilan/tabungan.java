    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        try{
            String sql="UPDATE simpanan set nik=?, nama=?, saldo_sekarang=?, tanggal=?, tabungan_masuk=?, tabungan_keluar=?, saldo=? where kode_tabungan=?";
            PreparedStatement stat= conn.prepareStatement(sql);
            
            
            stat.setString(1, tnik.getText());
            stat.setString(2, tnama.getText());
            stat.setString(3, tsaldosekarang.getText());
            stat.setString(4, ttanggal.getText());
            stat.setString(5, ttabunganmasuk.getText());
            stat.setString(6, "0");
            stat.setString(7, tsaldo.getText());
            stat.setString(8, tkodetabungan.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            tkodetabungan.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah"+e);
        }
    }//GEN-LAST:event_bubahActionPerformed

