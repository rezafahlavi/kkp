    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String sql="insert into simpanan value(?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat=conn.prepareStatement(sql);
            stat.setString(1, tkodetabungan.getText());
            stat.setString(2, tnik.getText());
            stat.setString(3, tnama.getText());
            stat.setString(4, tsaldosekarang.getText());
            stat.setString(5, ttanggal.getText());
            stat.setString(6, ttabunganmasuk.getText());
            stat.setString(7, "0");
            stat.setString(8, tsaldo.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data BERHASIL disimpan");
            kosong();
            tkodetabungan.requestFocus();
            datatable();
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data GAGAL disimpan");
        }
    }//GEN-LAST:event_bsimpanActionPerformed

